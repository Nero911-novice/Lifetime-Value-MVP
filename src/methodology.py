GLOBAL_METHODOLOGY = """
### Как устроено приложение как аналитическая система

1. **Слой исходных данных.**  
   Приложение использует четыре основные демонстрационные сущности:
   - `users` — регистрация, источник привлечения, стоимость привлечения, тип активации;
   - `trips` — созданные заказы, статусы завершения и отмен, GMV, выручка платформы, скидки, переменные затраты и contribution margin;
   - `marketing_touches` — история маркетинговых касаний и демонстрационный response;
   - `campaigns` — агрегированная рамка кампаний и офферов.

2. **Слой пользовательской витрины.**  
   На основе событий строится `user_mart`, где соединяются:
   - факт активации;
   - число созданных, завершённых и отменённых заказов;
   - исторический LTV на горизонтах 30/90/180/365 дней;
   - активность за последние 30/90 дней;
   - признаки риска, ценности и промо-зависимости;
   - response-показатели по касаниям.

3. **Логика экранов.**
   - **Обзор** показывает управленческий срез базы, текущий операционный контур и карту сегментов риска и ценности.
   - **Когорты** показывают качество новых активаций, удержание и накопление ценности по месяцам жизни.
     Когорта формируется как месяц первой завершённой поездки, а зрелость — как число полных месяцев наблюдения.
     На экране раздельно читаются retention, накопленный LTV, накопленная маржа, доля отмен и доля промо-поездок.
   - **Сегменты** переводят фактическую аналитику в операционные группы и демонстрируют будущую логику action layer.
   - **Карточка пользователя** раскрывает связь между исходными событиями и итоговыми агрегатами.
   - **Модель данных** фиксирует структуру сущностей, без которой переход к реальной БД будет неоднозначным.

4. **Принцип интерпретации.**  
   Числа в приложении следует читать как демонстрацию будущей аналитической логики:
   - исторический LTV основан на фактической марже, а не на GMV;
   - молодые когорты нельзя напрямую сравнивать со зрелыми без учёта maturity;
   - created orders и cancel rate показывают операционный слой спроса и исполнения;
   - доля отмен и промо-поездок критична для райд-хейлинга: они могут указывать на качество сервиса и цену роста;
   - response по касаниям не следует интерпретировать как причинный uplift;
   - rule-based сегменты не заменяют production-модели churn, pLTV и iLTV.

5. **Что запланировано после MVP.**  
   Следующий уровень зрелости:
   - подключение БД и SQL-витрин;
   - раздельные витрины по пользователям, заказам, поездкам и касаниям;
   - прогнозный LTV и survival-подход;
   - response-модели, uplift и сценарный decision layer;
   - управленческие сценарии по удержанию, реактивации и ограничениям промо.
"""


SEGMENTS_METHODOLOGY = """
1. **Связь с экраном «Обзор».**
   Экран «Сегменты» продолжает обзорные KPI и переводит общую картину в управляемые группы пользователей.

2. **Почему разделены ценность, риск и промо-зависимость.**
   Разделение осей нужно, чтобы не смешивать:
   - высокую активность и высокую ценность;
   - высокую выручку и высокую маржу;
   - устойчивое поведение и поведение, поддерживаемое промо.

3. **Что такое сегмент в этом MVP.**
   Сегменты строятся по rule-based логике и служат аналитической диагностикой. Они **не** являются прогнозом поведения и **не** отражают причинный эффект.

4. **Как экран помогает перейти к действиям.**
   `compound_segment` (risk × value) задаёт приоритет, а промо-зависимость уточняет тактику удержания, реактивации или ограничения стимулов.

5. **Ограничение recommended action.**
   Рекомендованные действия на этом экране — аналитические подсказки MVP, а не автоматизированный decision engine.
   Для production-слоя понадобятся отдельные модели pLTV/churn/uplift и контур бизнес-правил.
"""
